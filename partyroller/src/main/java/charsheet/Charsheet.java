package charsheet;

import user.User;

public class Charsheet {

    private String name;

    private String strength;

    private String mana;

    private User owner;

    public String getName() {
        return name;
    }

    public String getStrength() {
        return strength;
    }

    public String getMana() {
        return mana;
    }

    public User getOwner() {
        return owner;
    }

}
